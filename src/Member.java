public class Member {
	
	private String name;
	private String Phone;
	
	public Member(String name, String Phone) {
		super();
		this.name = name;
		this.Phone = Phone;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String Phone) {
		this.Phone = Phone;
	}
	
}

